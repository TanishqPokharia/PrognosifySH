class PatientCardData {
  final String name;
  final String age;
  final String gender;
  final String bmi;
  final String disease;
  final String notes;
  final String email;
  final String token;

  PatientCardData(
      {required this.name,
      required this.age,
      required this.gender,
      required this.bmi,
      required this.disease,
      required this.notes,
      required this.email,
      required this.token});
}
